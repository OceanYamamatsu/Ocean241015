text = """私が茶道を習い始めた動機は幼少の頃帰省のたびに叔母がお点前を見せてくれたためです。初めて触れるその世界は、幼心に静謐な美しさとして焼きついています。叔母は私に茶筅の振り方やお辞儀の仕方を教えてくれましたが、当時は深い意味を理解していたわけではありませんでした。それでも、静かな空気、畳の上に差し込む光、湯が釜で沸く微かな音を聞きながら、自然と心が落ち着いたことが記憶に残り、それが茶道への興味の原点となりました。
高校に進学する頃には、この体験に導かれるように自然と茶道部に入部しました。そこでの本格的な稽古に触れていく中で、茶道が単なる日本の伝統文化や作法を学ぶ行為ではないことを意識するようになりました。それは「心を整えるための学び」であり、日常生活の雑多な思いから離れ、稽古を通して、自己と向き合い、内面を深く見つめ直す時間を得ることができました。
大学入学の年は、世界中がコロナ禍という未曽有の状況にあり、人と直接向き合う機会が極めて限られていました。先が読めない状況は多くの人の気持ちを不安定にさせ、心のつながりの大切さが改めて問われる時期でした。そのような時代だからこそ、形や物質的な豊かさではなく、心を大切にする茶道を本格的に学びたいという思いが募り、祖母に先生を紹介してもらいました。
茶道を習う上で私が現在も特に大切にしている心構えは、第一に「心を静める」姿勢です。茶室に入る前に身なりを整えるように、気持ちもまた清らかにしておくことが、良き稽古、良きお茶を点てるための絶対条件だと感じています。
"""

# 1セルの文字数
chunk_size = 15

# 指定文字数で分割
chunks = [text[i:i+chunk_size] for i in range(0, len(text), chunk_size)]

# 20行 × 10列に整形（不足部分は空欄）
rows, cols = 20, 10
table = []
index = 0
for r in range(rows):
    row = []
    for c in range(cols):
        if index < len(chunks):
            row.append(chunks[index])
        else:
            row.append("")  # 空白
        index += 1
    table.append(row)

# 出力（表形式）
for row in table:
    print(" | ".join(f"{cell: <15}" for cell in row))
